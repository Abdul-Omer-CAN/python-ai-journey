# Today we start unsupervised learning.
# We will learn about KMeans Clustering today.
# KMeans categorizes data by how close the values are to each other. It drops a K in the data points and K acts like a magnet. Wherein it pulls
# the closest values to it. For e.g Netflix groups by user watching habits or Hospital groups by symptoms in patients or Banks group by spending pattern.
# We measure how good clusters are by 'inertia'. Lower inertia = points are tight and close together. High inertia = points are spread out Worse cluster.
